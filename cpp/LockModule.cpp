#include "LockModule.h"

//---------------------------------------------------------------------------//

extern ULONG g_cLocks = 0;

//---------------------------------------------------------------------------//

void LockModule()
{
     ::InterlockedIncrement(&g_cLocks);
}

//---------------------------------------------------------------------------//

void UnlockModule()
{
    ::InterlockedDecrement(&g_cLocks);
}

